public class Bottle {
    double quantity;
    String color;

    public Bottle(double quantity, String color) {
        this.quantity = quantity;
        this.color = color;
    }

    public double getQuantity() {
        return quantity;
    }

    public String getColor() {
        return color;
    }
    
}
